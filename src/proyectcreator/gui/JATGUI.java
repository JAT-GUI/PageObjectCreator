    private void openExistingRepositoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openExistingRepositoryActionPerformed
        // TODO add your handling code here:
        buscador.setDialogType(JFileChooser.OPEN_DIALOG);
        int seleccion = buscador.showOpenDialog(buscador);
        if(seleccion==JFileChooser.APPROVE_OPTION){
            File origen = buscador.getSelectedFile();
            JatFilesManager repositoryHistory= new JatFilesManager(null);
            repositoryHistory.writeNewLog(origen.getAbsolutePath());
            reDesignHistoryList();
            MultiCreatorGUI multiCreatorGUI= new MultiCreatorGUI(origen.getAbsolutePath().split(configFile)[0]);
            multiCreatorGUI.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_openExistingRepositoryActionPerformed

