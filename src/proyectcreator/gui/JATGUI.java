    private void reDesignHistoryList() {
        JatFilesManager repositoryHistory= new JatFilesManager(null);
        for (int i = 0; i < 5; i++) {
            labels[i].setText(repositoryHistory.readLog(repositoryHistory.getLenght()-i));
        }
    }
